void game_selectionmenu()
{
	int i, selection = 0, page = 0;

	int length = sizeof(game) / sizeof(game[0]);

MENU:
	while (true)
	{
		u32 pressed1;
		u32 pressed;

		WPAD_ScanPads();

		VIDEO_WaitVSync();

		resetscreen();

		pressed = WPAD_ButtonsDown(0);
		pressed1 = WPAD_ButtonsHeld(0);

		printf("\x1B[%d;%dH", 3, 0); // move console cursor to y/
		printf("Wii Shoplift Channel v1.0 - By Larsenv\tPress 2 to view a disclaimer\n\n");

		if (pressed == WPAD_BUTTON_2)
		{
			disclaimer();
			resetscreen();
			goto MENU;
		}

		for (i = page * 16; i < (page * 16) + 16; i++)
		{
			if (i < length)
			{
				printf("%s %s\n", ((selection == i) ? ARROW : "  "), game[i].title);
			}
		}

		if (pressed == WPAD_BUTTON_UP || pressed1 == WPAD_BUTTON_UP)
		{
			if (selection > 0)
			{
				selection--;

				if (selection % 16 == 0)
				{
					page--;
				}
			}
			else
			{
				selection = length - 1;
				page = length / 16;
			}
		}

		if (pressed == WPAD_BUTTON_DOWN || pressed1 == WPAD_BUTTON_DOWN)
		{
			if (selection < length)
			{
				selection++;

				if (selection % 16 == 0)
				{
					page++;
				}
			}
			else
			{
				selection = 0;
				page = 0;
			}
		}

		if (pressed == WPAD_BUTTON_RIGHT || pressed1 == WPAD_BUTTON_RIGHT)
		{
			if (selection < length && (page * 16) + 16 < length)
			{
				selection = (selection % 16) + (page * 16) + 16;
				page++;
			}
			else
			{
				selection = 0;
				page = 0;
			}
		}

		if (pressed == WPAD_BUTTON_LEFT || pressed1 == WPAD_BUTTON_LEFT)
		{
			if (selection > 0 && (page * 16) + 16 < length)
			{
				selection = (selection % 16) + (page * 16) - 16;
				page--;
			}
			else
			{
				selection = length - 1;
				page = length / 16;
			}
		}

		if (pressed == WPAD_BUTTON_A)
		{
			char title[49];
			sprintf(title, "/oss/serv/B_09.jsp?titleId=%016llx", game[selection].id);
			WII_LaunchTitleWithArgs(0x0001000248414241LL, 0, title, NULL);
		}

		if (pressed == WPAD_BUTTON_HOME)
		{
			printf("exiting");
			return;
		}
	}
}

//---------------------------------------------------------------------------------
int main(int argc, char **argv)
{
	//---------------------------------------------------------------------------------

	// Initialise the video system
	VIDEO_Init();

	// This function initialises the attached controllers
	WPAD_Init();

	// Obtain the preferred video mode from the system
	// This will correspond to the settings in the Wii menu
	rmode = VIDEO_GetPreferredMode(NULL);

	// Allocate memory for the display in the uncached region
	xfb = MEM_K0_TO_K1(SYS_AllocateFramebuffer(rmode));

	// Initialise the console, required for printf
	console_init(xfb, 20, 20, rmode->fbWidth, rmode->xfbHeight, rmode->fbWidth * VI_DISPLAY_PIX_SZ);
	// SYS_STDIO_Report(true);

	// Set up the video registers with the chosen mode
	VIDEO_Configure(rmode);

	// Tell the video hardware where our display memory is
	VIDEO_SetNextFramebuffer(xfb);

	// Make the display visible
	VIDEO_SetBlack(false);

	// Flush the video register changes to the hardware
	VIDEO_Flush();

	// Wait for Video setup to complete
	VIDEO_WaitVSync();
	if (rmode->viTVMode & VI_NON_INTERLACE)
		VIDEO_WaitVSync();

	PAD_Init();
	WPAD_Init();

	game_selectionmenu();

	return 0;
}
